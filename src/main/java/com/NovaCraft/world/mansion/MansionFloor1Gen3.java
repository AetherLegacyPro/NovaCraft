    private Block determineIfDarkOakFenceExists(World world, int x, int y, int z) {
        Block existingBlock = world.getBlock(x, y, z);
        if (Configs.enableDarkOakFenceInAncientCity && Loader.isModLoaded("etfuturum")) {
            try {
                Block fence_dark_oak = GameRegistry.findBlock("etfuturum", "fence_dark_oak");
                if (fence_dark_oak != null && (existingBlock == null || existingBlock != fence_dark_oak)) {
                    return fence_dark_oak;
                }
            } catch (Exception ex) {
                if (Configs.enableDebugMode) {
                    System.out.println("Missing Dark Oak Fence from Et Futurum Requiem, using regular fence instead...");
                }
                return Blocks.fence;
            }
        }

        return Blocks.fence;
    }

    private Block determineIfBannerExists(World world, int x, int y, int z) {
        Block existingBlock = world.getBlock(x, y, z);
        if (Configs.enableBanners && Loader.isModLoaded("etfuturum")) {
            try {
                Block banner = GameRegistry.findBlock("etfuturum", "banner");
                if (banner != null && (existingBlock == null || existingBlock != banner)) {
                    return banner;
                }
            } catch (Exception ex) {
                if (Configs.enableDebugMode) {
                    System.out.println("Missing Banners from Et Futurum Requiem...");
                }
                return Blocks.air;
            }
        }

        return Blocks.air;
    }

