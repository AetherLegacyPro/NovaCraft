    private Block placeTiles(Random random) {
        if (random.nextDouble() < 0.62) {
            try {
                Block deepslate_bricks = GameRegistry.findBlock("etfuturum", "deepslate_bricks");
                if (deepslate_bricks != null ) {
                    return deepslate_bricks;
                }
            } catch (Exception ex) {
                return NovaCraftBlocks.nullstone_tiles;
            }
        } else {
            return NovaCraftBlocks.nullstone_tiles;
        }
        return NovaCraftBlocks.nullstone_tiles;
    }

    private Block placeStoneBricks(Random random) {
        if (random.nextDouble() < 0.65) {
            return Blocks.stonebrick;
        } else {
            return NovaCraftBlocks.grimstone_bricks;
        }
    }

    private Block placeGemstonePile(Random random) {
        if (random.nextDouble() < 0.15) {
            return NovaCraftBlocks.iron_pile;
        } else if (random.nextDouble() >= 0.15 && random.nextDouble() < 0.30) {
            return NovaCraftBlocks.gold_pile;
        } else if (random.nextDouble() >= 0.30 && random.nextDouble() < 0.45) {
            return NovaCraftBlocks.lapis_pile;
        } else if (random.nextDouble() >= 0.45 && random.nextDouble() < 0.60) {
            return NovaCraftBlocks.crystal_pile;
        } else if (random.nextDouble() >= 0.60 && random.nextDouble() < 0.75) {
            return NovaCraftBlocks.vanite_pile;
        } else if (random.nextDouble() >= 0.75 && random.nextDouble() < 0.90) {
            if (Configs.enableTreasureCratesDropDiamonds) {
                return NovaCraftBlocks.diamond_pile;
            } else {
                return NovaCraftBlocks.crystal_pile;
            }
        } else {
            return NovaCraftBlocks.emerald_pile;
        }
    }

