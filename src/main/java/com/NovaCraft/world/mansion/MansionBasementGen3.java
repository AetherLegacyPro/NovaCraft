    private Block placeStoneBricks(Random random) {
        if (random.nextDouble() < 0.65) {
            return Blocks.stonebrick;
        } else {
            return NovaCraftBlocks.grimstone_bricks;
        }
    }

    private Block placeGemstonePile(Random random) {
        if (random.nextDouble() < 0.15) {
            return NovaCraftBlocks.iron_pile;
        } else if (random.nextDouble() >= 0.15 && random.nextDouble() < 0.30) {
            return NovaCraftBlocks.gold_pile;
        } else if (random.nextDouble() >= 0.30 && random.nextDouble() < 0.45) {
            return NovaCraftBlocks.lapis_pile;
        } else if (random.nextDouble() >= 0.45 && random.nextDouble() < 0.60) {
            return NovaCraftBlocks.crystal_pile;
        } else if (random.nextDouble() >= 0.60 && random.nextDouble() < 0.75) {
            return NovaCraftBlocks.vanite_pile;
        } else if (random.nextDouble() >= 0.75 && random.nextDouble() < 0.90) {
            if (Configs.enableTreasureCratesDropDiamonds) {
                return NovaCraftBlocks.diamond_pile;
            } else {
                return NovaCraftBlocks.crystal_pile;
            }
        } else {
            return NovaCraftBlocks.emerald_pile;
        }
    }

    private ItemStack getBookLoot(Random random) {
        int item = random.nextInt(10);
        switch (item) {
            case 0:
                return new ItemStack(Items.paper, random.nextInt(24) + 8);
            case 1:
                return new ItemStack(Items.redstone, random.nextInt(8) + 3);
            case 2:
                return new ItemStack(Items.book, random.nextInt(10) + 6);
            case 3:
                Enchantment enchantment2 = Enchantment.enchantmentsBookList[random.nextInt(Enchantment.enchantmentsBookList.length)];
                int maxLevel2 = enchantment2.getMaxLevel();
                ItemStack itemStack2 = Items.enchanted_book.getEnchantedItemStack(new EnchantmentData(enchantment2, maxLevel2));

                return itemStack2;
            case 4:
                return new ItemStack(NovaCraftItems.iridium_nugget, random.nextInt(4) + 3, 1);
            case 5:
                Enchantment enchantment = Enchantment.enchantmentsBookList[random.nextInt(Enchantment.enchantmentsBookList.length)];
                int maxLevel = enchantment.getMaxLevel();
                ItemStack itemStack = Items.enchanted_book.getEnchantedItemStack(new EnchantmentData(enchantment, maxLevel));

                return itemStack;
            default: {
                return new ItemStack(Items.paper, random.nextInt(32) + 16);
            }
        }
    }

