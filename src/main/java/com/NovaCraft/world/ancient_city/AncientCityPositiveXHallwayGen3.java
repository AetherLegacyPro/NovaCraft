	private Block determineIfSmoothBasaltExists(World world, int x, int y, int z) {
		Block existingBlock = world.getBlock(x, y, z);

		if (!(Configs.disableNetherliciousSmoothBasaltInAncientCity) && Loader.isModLoaded("netherlicious")) {
			try {
				Block nether_smooth_basalt = GameRegistry.findBlock("netherlicious", "BasaltBricks");
				if (nether_smooth_basalt != null && (existingBlock == null || existingBlock != nether_smooth_basalt)) {
					return nether_smooth_basalt;
				}
			} catch (Exception ex) {
				if (Configs.enableDebugMode) {
					System.out.println("Failed to Find Netherlicious smooth basalt, trying Et Futurum Requiem...");
				}
			}
		}

		if (Loader.isModLoaded("etfuturum")) {
			try {
				Block et_smooth_basalt = GameRegistry.findBlock("etfuturum", "smooth_basalt");
				if (et_smooth_basalt != null && (existingBlock == null || existingBlock != et_smooth_basalt)) {
					return et_smooth_basalt;
				}
			} catch (Exception ex) {
				if (Configs.enableDebugMode) {
					System.out.println("Failed to Find Et Futurum Requiem smooth basalt, placing Polished Vanite Bricks...");
				}
			}
		}

		return NovaCraftBlocks.polished_vanite_bricks;
	}
    
