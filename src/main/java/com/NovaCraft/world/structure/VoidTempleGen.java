    private Block placeBuildingBlock(Random random) {
        if (random.nextDouble() < 0.54) {
            return NovaCraftBlocks.nullstone_bricks;
        } else if (random.nextDouble() >= 0.54 && random.nextDouble() < 0.60) {
            return NovaCraftBlocks.cracked_nullstone_bricks;
        } else if (random.nextDouble() >= 0.60 && random.nextDouble() < 0.94) {
            return NovaCraftBlocks.grimstone_bricks;
        } else if (random.nextDouble() >= 0.94 && random.nextDouble() < 0.98) {
            return NovaCraftBlocks.cracked_grimstone_bricks;
        } else if (random.nextDouble() >= 0.98 && random.nextDouble() < 0.99) {
            return NovaCraftBlocks.sculk_bricks;
        } else {
            return NovaCraftBlocks.nullstone_bricks;
        }
    }

