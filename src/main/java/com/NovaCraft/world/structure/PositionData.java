package com.NovaCraft.world.structure;

public class PositionData {

	private int x, y, z;

	public PositionData(int x, int y, int z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}

	public int getX() {
		return this.x;
	}

	public int getY() {
		return this.y;
	}

	public int getZ() {
		return this.z;
	}

}