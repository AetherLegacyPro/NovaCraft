    private ItemStack getLoot(Random random) {
        int item = random.nextInt(15);
        switch (item) {
            case 0:
                return new ItemStack(Items.gunpowder, random.nextInt(2) + 2);
            case 1:
                return new ItemStack(Items.bone, random.nextInt(7) + 5);
            case 2:
                return new ItemStack(Items.string, random.nextInt(3) + 3);
            case 3:
                return new ItemStack(Items.feather, random.nextInt(2) + 1);
            case 4:
                return new ItemStack(Items.stick, random.nextInt(7) + 6);
            case 5:
                if (Configs.enableTreasureCratesDropDiamonds) {
                    return new ItemStack(NovaCraftItems.diamond_nugget, random.nextInt(2) + 3, 1);
                } else {
                    return new ItemStack(NovaCraftItems.iridium_nugget, random.nextInt(2) + 3, 1);
                }
            case 6:
                return new ItemStack(NovaCraftItems.iridium_nugget, random.nextInt(2) + 3, 1);
            case 7:
                return new ItemStack(Items.gold_nugget, random.nextInt(3) + 4);
            case 8:
                return new ItemStack(Items.coal, random.nextInt(2) + 5);
            case 9:
                return new ItemStack(NovaCraftItems.vanite_ingot, 1);
            case 10:
                return new ItemStack(Items.redstone, random.nextInt(2) + 1);
            case 11:
                int chance = random.nextInt(17);
                if (chance == 0) {
                    return new ItemStack(Blocks.skull, 1, 1);
                } else if (chance == 1) {
                    return new ItemStack(NovaCraftItems.tsavorokite_shard, 1);
                } else if (chance == 2) {
                    return new ItemStack(Items.iron_pickaxe, 1);
                } else if (chance == 3) {
                    return new ItemStack(Items.iron_shovel, 1);
                } else if (chance == 4) {
                    return new ItemStack(Items.iron_axe, 1);
                } else if (chance == 5) {
                    return new ItemStack(Items.iron_sword, 1);
                } else if (chance == 6) {
                    return new ItemStack(NovaCraftBlocks.torchflower, 1);
                } else if (chance >= 7 && chance <= 10) {
                    return new ItemStack(NovaCraftBlocks.calvatia_mushroom, 1);
                } else if (chance >= 10 && chance <= 13) {
                    return new ItemStack(NovaCraftBlocks.hydnum_mushroom, 1);
                } else if (chance >= 14 && chance <= 17) {
                    return new ItemStack(NovaCraftBlocks.pleurotus_mushroom, 1);
                }
            default: {
                return new ItemStack(Items.bone, random.nextInt(7) + 5);
            }
        }
    }

    private Block placeSandstone(Random random) {
        if (random.nextDouble() < 0.12) {
            return Blocks.air;
        } else {
            return Blocks.sandstone;
        }
    }
